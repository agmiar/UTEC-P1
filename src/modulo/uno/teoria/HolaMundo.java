package modulo.uno.teoria;

public class HolaMundo {
    public static void main(String[] args) {
        System.out.println("Hola Mundo");
        int i = 15_000_000;
        System.out.println(i * 9);

        // ASCII
        char arroba = 64;
        System.out.println(arroba);

        // Unicode
        char euro = '\u20AC';
        System.out.println(euro);
    }
}
