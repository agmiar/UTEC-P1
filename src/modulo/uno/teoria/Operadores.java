package modulo.uno.teoria;

public class Operadores {
    public static void main(String[] args) {
        float a = 2;
        float b = 3;
        System.out.println(a + b); // Imprime 5.0
        System.out.println(a - b); // Imprime -1.0
        System.out.println(a * b); // Imprime 6.0
        System.out.println(a / b); // Imprime 0.666...
        System.out.println(b % a); // Imprime 1.0
        System.out.println(Math.pow(a, b)); // Imprime 8.0
    }
}
